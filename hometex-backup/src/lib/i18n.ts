export function getTranslation(lang: Language, key: string): string {
  const keys = key.split('.');
  let value: any = translations[lang] || translations['en'];
  for (const k of keys) {
    value = value?.[k];
  }
  return value || key;
}

export function getCategoryName(category: any, lang: Language): string {
  const nameKey = `name_${lang}`;
  return category[nameKey] || category.name_en || category.name || '';
}

export function getCategoryDescription(category: any, lang: Language): string {
  const descKey = `description_${lang}`;
  return category[descKey] || category.description_en || category.description || '';
}
